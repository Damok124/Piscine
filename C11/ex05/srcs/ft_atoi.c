#include "do_op.h"

int	ft_atoi(char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 1;
	while (*str && ((*str >= '\t' && *str <= '\r') || *str == ' '))
		str++;
	while (*str && (*str == '+' || *str == '-'))
	{
		if (*str == '-')
			k *= -1;
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		if (i != 0)
			i *= 10;
		i += *str - '0';
		str++;
	}
	return (i * k);
}
