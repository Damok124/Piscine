#include "do_op.h"

int	ft_def_op(char *hs, char *nd)
{
	int	i;

	i = 0;
	while (nd[i])
		i++;
	if (i != 1)
		return (5);
	i = -1;
	while (hs[++i])
	{
		if (hs[i] == nd[0])
			return (i);
	}
	return (5);
}
