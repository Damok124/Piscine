int	ft_str_is_alpha(char *str)
{
	int		i;
	char	a;

	i = 0;
	while (str[i] != '\0')
	{
		a = str[i];
		if (a < 65 || a > 122)
			return (0);
		if (a > 90 && a < 97)
			return (0);
		i++;
	}
	return (1);
}
